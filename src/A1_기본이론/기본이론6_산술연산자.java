package A1_기본이론;

public class 기본이론6_산술연산자 {

	public static void main(String[] args) {
		System.out.println("[산술연산자] + - * / % ");
		System.out.println(10+3);
		System.out.println(10-3);
		System.out.println(10*3);
		System.out.println(10/3);
		System.out.println(10%3);
		System.out.println(10/3.0);
		
		System.out.println("[연산자우선순위]");
		System.out.println(10+3*3);
		System.out.println((10+3)*3);

	}

}
