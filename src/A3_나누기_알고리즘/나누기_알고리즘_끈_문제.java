package A3_나누기_알고리즘;

public class 나누기_알고리즘_끈_문제 {
	
	public static void main(String[] args) {
		/*
		 * [문제]
		 * 
		 	끈 5/8m 를 모두 사용하여 정사각형 모양한개를 만들었습니다 
		 	이 정사각형의 한변의 길이는 몇 m 입니까?
		 */
		
	/*
	 	총 끈의 길이 5/8m
	 	정사각형의 변의 수 4
	 	정사각형의 변의 길이가 같으므로 한변의 길이 = 총길이/변의수 = 5/8/4
	 */
		System.out.println(5.0/8.0/4.0);
	}
	
}