package A3_나누기_알고리즘;

public class 나누기_알고리즘_밤_문제 {
	public static void main(String[] args) {	
		/*		
		 * 	[문제]
		 * 		철수가 1시간 40분동안 일정한 빠르기로 주은 밤의 무게가 11 kg 이다.
		 * 		3분동안 주은밤은 몇kg 인지 구하시오. (소수점)
				
		*/	
		/*
		 	1시간40분 = 100분
		 	1분 동안 주은 밤의 개수 = 11kg/100분
		 	3분동안 주은 밤의 개수 = 3분*(11kg/100분)
		 */
		System.out.println(11.0/100*3);
     }
}